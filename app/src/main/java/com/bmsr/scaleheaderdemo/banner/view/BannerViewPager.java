package com.bmsr.scaleheaderdemo.banner.view;

import android.content.Context;
import android.util.AttributeSet;
import android.util.Log;
import android.view.MotionEvent;
import android.view.View;

import androidx.viewpager.widget.ViewPager;

import com.bmsr.scaleheaderdemo.banner.Banner;


public class BannerViewPager extends ViewPager {
    private boolean scrollable = true;
    public static boolean isScrolling;

    public BannerViewPager(Context context) {
        super(context);
    }

    public BannerViewPager(Context context, AttributeSet attrs) {
        super(context, attrs);
    }

    @Override
    public boolean onTouchEvent(MotionEvent ev) {
        if(this.scrollable) {
            if (getCurrentItem() == 0 && getChildCount() == 0) {
                return false;
            }
            return super.onTouchEvent(ev);
        } else {
            return false;
        }
    }

    @Override
    public boolean onInterceptTouchEvent(MotionEvent ev) {
        if(this.scrollable) {
            if (getCurrentItem() == 0 && getChildCount() == 0) {
                return false;
            }
            return super.onInterceptTouchEvent(ev);
        } else {
            return false;
        }
    }

    public void setScrollable(boolean scrollable) {
        this.scrollable = scrollable;
    }


    @Override
    protected void onLayout(boolean changed, int l, int t, int r, int b) {
        super.onLayout(changed, l, t, r, b);
        Banner.BannerPagerAdapter adapter = (Banner.BannerPagerAdapter) getAdapter();
        View view = adapter.getCurrentView();
        if (isScrolling) {
            view.layout(l, t, r, b);
            Log.i("wdd", "onLayout: ---->1111111");
        }
    }
}
